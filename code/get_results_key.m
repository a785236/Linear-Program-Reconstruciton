%% Maps db_size,num_primes,integer_programming,...
%% error_sigma,lp_type,trial,dini_bound)
%% to the string used as a key in the results struct.

function key = get_results_key(db_size,num_primes,integer_programming,error_sigma,lp_type,trial,dini_bound)
    key_string = strcat("size=",string(db_size),":",...
                                    "num_primes=",string(num_primes),":",...
                                    "integer_programming=",string(integer_programming),":",...
                                    "error_sigma=",string(error_sigma),":",...
                                    "lp_type=",string(lp_type),":",...
                                    "trial=",string(trial),":",...
                                    "dini_bound=",string(dini_bound));
    key = char(key_string);
end